//Concatenar x Interpolar:
// Vamos criar variaveis usando let, const e var, atribuir strings e vamos concatenar e interpolar.

let age = 22;
const NAME = "Ariel";
var state = "São Paulo";

//Concatenação:
console.log(
  "Olá, eu sou o " + NAME + " tenho " + age + " anos e moro em " + state
);

//Interpolação:
console.log(`Olá, eu sou o ${NAME} tenho ${age} anos e moro em ${state}.`);
